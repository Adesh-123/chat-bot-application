import React, { useState,useEffect} from "react";
import styled from "styled-components";
import {useHistory} from "react-router-dom";
import Logo from "../assets/logo.svg";
import { ToastContainer, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import axios from "axios"

export default function Login() {
  const history=useHistory();
  useEffect(() => {
    if(localStorage.getItem("user")){
       history.push("/");
    }
  }, [])
  

  const [values, setValues] = useState({ username: "", password: "" });
  const toastOptions = {
    position: "bottom-right",
    autoClose: 8000,
    pauseOnHover: true,
    draggable: true,
    theme: "dark",
  };

  const handleChange = (event) => {
    setValues({ ...values, [event.target.name]: event.target.value });
  };

  const validateForm = () => {
    const { username, password } = values;
    if (username === "") {
      toast.error("Email and Password is required.", toastOptions);
      return false;
    } else if (password === "") {
      toast.error("Email and Password is required.", toastOptions);
      return false;
    }
    return true;
  };

  const handleSubmit = async (event) => {
    event.preventDefault();
    if(validateForm()){
      try{
        const res =await axios.post("https://messging-application.herokuapp.com/api/auth/login",values);
        if(res.data.status){
            toast.success("Successfully Registered",toastOptions);
            localStorage.setItem("user",JSON.stringify(res.data.user));
            history.push("/");
            window.location.reload(false);
        }else{
          toast.error(res.data.msg,toastOptions);
        }
      }catch(err){
        toast.error("Server error please try later",toastOptions);
      }
    }
  };
  const handleclick=()=>{
    history.push("/register");
    window.location.reload(false);
  }

  return (
    <>
      <FormContainer>
        <form action="" onSubmit={(event) => handleSubmit(event)}>
          <div className="brand">
            <img src={Logo} alt="logo" />
            <h1>snappy</h1>
          </div>
          <input
            type="text"
            placeholder="Username"
            name="username"
            onChange={(e) => handleChange(e)}
            min="3"
          />
          <input
            type="password"
            placeholder="Password"
            name="password"
            onChange={(e) => handleChange(e)}
          />
          <button type="submit">Log In</button>
          <span>
            Don't have an account ? <span onClick={handleclick}>Create One.</span>
          </span>
        </form>
      </FormContainer>
      <ToastContainer />
    </>
  );
}

